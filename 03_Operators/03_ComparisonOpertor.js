// Comparison Operators :-

// Input ==> Number, String 
// Output ==> Boolean

// There are four types of comparison operators :-

// 1. == (Double equal to)

console.log(5 == 6) // false
console.log(2 == 2) // true
console.log(3 == 4) //false

// 2. != (Not equal to)

console.log(2 != 2) // false
console.log(2 != 3) // true
console.log("Apple" != "Mango") // true 

// 3. === (Triple equal to)

console.log("2" === 2) // false
console.log(2 === 2) // true
console.log("apple" === "mango") // false

// 4. !== (Not double equal to)

console.log("2" !== 2) // true
console.log("3" !== "2") // true
console.log("2" !== "2") // false