// Arthmethic Operators

let num1 = 15
let num2 = 10

// Addition(+)
console.log(num1 + num2) // 25

// Subtraction(-)
console.log(num1 - num2) // 5

// Multiplication(*)
console.log(num1 * num2) // 150

// Division(/)
console.log(num1 / num2) // 1.5

// Modulus(%)
console.log(num1 % num2) // 5

// Exponentiation(**)
console.log(num1 ** num2) // 576650390625

