// RELATIONAL OPERATOR

// Relation Operators are divided into 4 types :-

// 1. < (Less than)
// 2. <= (Less than equal to)
// 3. > (Greater than)
// 4. >= (Greater than equal to)

// they will only work with Number 
// Input ==> Number , Output ==> Boolean

let num1 = 10
let num2 = 20

console.log(num1 < num2) // true
console.log(num1 <= num2) // ture
console.log(num1 > num2) // false
console.log(num1 >= num2) // false
